public class Person {

    //fields
    private String name;
    private int age;

    //method or constructor
    public Person(String name, int age) {
        this.name = name;
        this.age = age;
    }

    //getter and setter
    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}


